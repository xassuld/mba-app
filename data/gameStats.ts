export function getGameStats(gameId: string): GameDetailStats | undefined {
  const cached = gameStatsById[gameId];
  if (cached) return cached;

  const game = getGameById(gameId);
  if (!game || game.status === "upcoming") return undefined;

  return buildSyntheticGameStats(game) ?? undefined;
}
